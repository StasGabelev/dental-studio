-- Раунд 3 — імена що залишились після першого та другого проходу
UPDATE cc_patients
SET gender = CASE

  WHEN SPLIT_PART(full_name, ' ', 2) IN (
    -- ══ ЖІНОЧІ ══
    -- Олександра (57!) — жіноча форма від Олександр
    'Олександра','Александра',
    -- Ярослава, Владислава — жіночі форми
    'Ярослава','Владислава','Владислава',
    -- Анастасія — варіанти написання
    'Анастасия','Анастасія',
    -- Єлизавета — варіанти
    'Єлізавета','Елізавета','Єлизавета','Єлізавета','Лізавета',
    -- Каріна — варіант з І
    'Каріна',
    -- Віталіна
    'Віталіна','Виталина','Биталина',
    -- Влада
    'Влада',
    -- Алена/Альона — варіанти
    'Алена','Алёна','Аліна',
    -- Інна/Инна — кирилична варіація
    'Инна',
    -- Єсенія
    'Єсєнія','Єсенія','Есения','Есенія',
    -- Милана — без наголосу
    'Милана',
    -- Томара — варіант Тамара
    'Томара',
    -- Амелія
    'Амелія','Амелия',
    -- Дар''я — з апострофом різних видів
    'Дар''я','Дарья','Дар'||chr(8217)||'я','Дар'||chr(8216)||'я',
    -- Даша
    'Даша',
    -- Мія
    'Мія','Мия',
    -- Еліна — варіант Аліни
    'Еліна','Елина',
    -- Наташа
    'Наташа',
    -- Мар''яна
    'Мар''яна','Мар'||chr(8217)||'яна','Мар'||chr(8216)||'яна','Мар''яна',
    -- Романа — жіноча форма
    'Романа',
    -- Таісія — варіант Таїсія
    'Таісія','Таисия',
    -- Весна
    'Весна',
    -- Ніка
    'Ніка','Ника',
    -- Тереза
    'Тереза',
    -- Руфіна
    'Руфіна','Руфина',
    -- Божена
    'Божена'
  ) THEN 'F'

  WHEN SPLIT_PART(full_name, ' ', 2) IN (
    -- ══ ЧОЛОВІЧІ ══
    -- Нікіта (37) — українська форма Микити
    'Нікіта','Никіта',
    -- Євгеній (31) — варіант Євген
    'Євгеній','Евгеній',
    -- Віталій (23)
    'Віталій','Виталий','Виталій',
    -- Кирил (21) — скорочена форма Кирило
    'Кирил','Кіріл',
    -- Святослав (14)
    'Святослав',
    -- Константін — варіант з І
    'Константін',
    -- Савелій (4)
    'Савелій','Савелий',
    -- Данил (4) — варіант Данило
    'Данил','Данило','Данила',
    -- Станіслав (4)
    'Станіслав','Станислав',
    -- Дмитрій (3) — варіант Дмитро
    'Дмитрій','Дмітрій',
    -- Ян (3)
    'Ян',
    -- Генадій — варіант з одним н
    'Генадій','Генадий',
    -- Давід (2) — варіант Давид
    'Давід',
    -- Дамір/Дамир (2)
    'Дамір','Дамир',
    -- Єлісей (2)
    'Єлісей','Елісей','Елисей',
    -- Ілля/Илья — кирилична варіація
    'Илья','Ілля'
  ) THEN 'M'

  ELSE gender
END
WHERE (gender IS NULL OR gender NOT IN ('M', 'F'))
  AND TRIM(SPLIT_PART(full_name, ' ', 2)) != '';

-- Підсумок
SELECT gender, COUNT(*) FROM cc_patients GROUP BY gender ORDER BY COUNT(*) DESC;
