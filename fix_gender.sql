-- КРОК 2: Результат — скільки оновлено
-- ══════════════════════════════════════════
SELECT
  COALESCE(gender, 'NULL / не визначено') AS gender,
  COUNT(*) AS count
FROM cc_patients
GROUP BY gender
ORDER BY count DESC;


-- ══════════════════════════════════════════
-- КРОК 3: Імена що залишились без статі
-- Скиньте цей список розробнику — він додасть їх в словник
-- ══════════════════════════════════════════
SELECT
  SPLIT_PART(full_name, ' ', 2) AS first_name,
  COUNT(*) AS patients_count
FROM cc_patients
WHERE (gender IS NULL OR gender NOT IN ('M', 'F'))
  AND TRIM(SPLIT_PART(full_name, ' ', 2)) != ''
GROUP BY 1
ORDER BY 2 DESC
LIMIT 100;
