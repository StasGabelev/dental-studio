UPDATE cc_patients
SET gender = CASE

  WHEN SPLIT_PART(full_name, ' ', 2) IN (
    'Наталия','Наталья',
    'Алеся',
    'Анжелика','Анжеліка',
    'Варя',
    'Антоніна','Антонина',
    'Таміла','Тамила',
    'Ріта','Рита',
    'Міра','Мира',
    'Хрестина',
    'Рената',
    'Кира',
    'Еріка','Ерика',
    'Мішель','Мишель',
    'Гелена',
    'Євенія','Евения',
    'Катерна',
    'Вераніка','Вероніка',
    'Аніта','Анита',
    'Лида',
    'Олексадра',
    'Юліана','Юлиана',
    'Індіра','Индира',
    'Владіслава'
  ) THEN 'F'

  WHEN SPLIT_PART(full_name, ' ', 2) IN (
    'Константин','Константін',
    'Михаїл','Михаил',
    'Тимофій','Тимофий',
    'Макарій','Макарий',
    'Ілля','Илья',
    'Виталий','Виталій','Віталій',
    'Василій','Василий',
    'Даніл','Данил',
    'Яшар',
    'Мурат',
    'Ігнат','Игнат',
    'Єгеній','Єгений',
    'Домінік','Доминик',
    'Артемій','Артемий'
  ) THEN 'M'

  ELSE gender
END
WHERE (gender IS NULL OR gender NOT IN ('M', 'F'))
  AND TRIM(SPLIT_PART(full_name, ' ', 2)) != '';

-- Геннадій(1983р.) — брудний запис з роком народження в імені
UPDATE cc_patients SET gender = 'M'
WHERE SPLIT_PART(full_name, ' ', 2) LIKE 'Геннадій%'
  AND (gender IS NULL OR gender NOT IN ('M', 'F'));

SELECT gender, COUNT(*) FROM cc_patients GROUP BY gender ORDER BY COUNT(*) DESC;
